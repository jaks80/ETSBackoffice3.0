    private void buttonSearchActionPerformed(ActionEvent event) {

        String ticketStatus = (String) cmbTicketStatus.getSelectedItem();
        String airLineCode = txtCareerCode.getText();
        String ticketingAgtOid = txtOfficeId.getText();

        Date from = dtFrom.getDate();
        Date to = dtTo.getDate();

        if ("All".equals(ticketStatus)) {
            ticketStatus = null;
        }

        if (airLineCode.isEmpty()) {
            airLineCode = null;
        }

        if (airLineCode == null || ticketingAgtOid.isEmpty()) {
            ticketingAgtOid = null;
        }

        if (airLineCode == null || airLineCode.isEmpty()) {
            airLineCode = null;
        }

        progressBar.setValue(0);

        task = new SegmentReportTask(ticketStatus, airLineCode, from, to, ticketingAgtOid);
        task.addPropertyChangeListener(this);
        task.execute();
    }

    private void populateTblSegment() {
        SegmentReport report = task.getReport();
       
        List<Segment> list = report.getList();
        DefaultTableModel tableModel = (DefaultTableModel) tblSegment.getModel();
        tableModel.getDataVector().removeAllElements();
        tblSegment.repaint();
        int row =0;
        for(Segment s: list){
         tableModel.insertRow(row, new Object[]{s.getTravelDate(),s.getOutCity(),s.getInCity(),s.getFlightNo(),
             s.getAirLine(),s.getTicketClass(),s.getBookingOid(),s.getTicketingOid(),s.getPnr(),
             s.getTicketNo(),s.getTktStatus(),s.getCount()});
            row++;
        }
        
        lblIssue.setText(report.getTotalIssuedSegment().toString());
        lblCancell.setText(report.getTotalRefundedSegment().toString());
        lblOpen.setText(report.getTotalOpenSegment().toString());
        lblVoid.setText(report.getTotalVoidSegment().toString());
        lblReissue.setText(report.getTotalReissuedSegment().toString());
        lblBalance.setText(report.getSegBalance().toString());
        btnSearch.setEnabled(true);
    }
    
