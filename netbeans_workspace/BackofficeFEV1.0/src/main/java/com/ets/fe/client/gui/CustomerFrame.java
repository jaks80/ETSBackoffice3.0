    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        Window w = SwingUtilities.getWindowAncestor(this);
        Frame owner = w instanceof Frame ? (Frame) w : null;
        CustomerDlg dlg = new CustomerDlg(owner);
        dlg.setLocationRelativeTo(this);
        dlg.setTitle("Edit Customer");
        Customer customer = null;

        if (tblCustomer.getSelectedRow() != -1) {
            int row = tblCustomer.getSelectedRow();
            customer = customers.getList().get(row);

            if (dlg.showDialog(customer)) {
                CustomerTask task = new CustomerTask(customer);
                task.execute();
            }
        } else {
            lblInfo.setText("Select a customer from table to Edit");
        }
    }//GEN-LAST:event_btnEditActionPerformed

