    @Override
    public void propertyChange(PropertyChangeEvent evt) {
        if ("progress".equals(evt.getPropertyName())) {
            int progress = (Integer) evt.getNewValue();
            progressBar.setValue(progress);
            if (progress == 100) {
                try {
                    List<ProductivityReport> reports = task.get();

                    for (ProductivityReport r : reports) {
                        if (r.getProductivityLine().isEmpty()) {
                            continue;
                        }

                        if (r.getSaleType().equals(Enums.SaleType.TKTSALES)) {
                            treport = r;
                        } else if (r.getSaleType().equals(Enums.SaleType.OTHERSALES)) {
                            oreport = r;
                        }
                    }
                    populateTableTicket();
                    populateTableOther();
                } catch (InterruptedException | ExecutionException ex) {
                    Logger.getLogger(TSalesInvoiceReportingFrame.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    btnSearch.setEnabled(true);
                }
            }
        }
    }
