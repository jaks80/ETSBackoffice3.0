    private void search() {
        btnSearch.setEnabled(false);
        Date from = dtFrom.getDate();
        Date to = dtTo.getDate();
        task = new UserProductivityTask(from, to, progressBar);
        task.addPropertyChangeListener(this);
        task.execute();
    }

    private void populateTableTicket() {

        DefaultTableModel tableModel = (DefaultTableModel) tblTicketing.getModel();
        tableModel.getDataVector().removeAllElements();

        if (treport == null) {
            return;
        }
        List<ProductivityLine> tlines = treport.getProductivityLine();
        if (tlines.size() > 0) {
            int i = 0;
            for (ProductivityLine line : tlines) {
                tableModel.insertRow(i, new Object[]{line.getKey(), line.getValue()});
                i++;
            }
        }
    }

    private void populateTableOther() {

        DefaultTableModel tableModel = (DefaultTableModel) tblOther.getModel();
        tableModel.getDataVector().removeAllElements();

        if (oreport == null) {
            return;
        }
        List<ProductivityLine> olines = oreport.getProductivityLine();
        if (olines.size() > 0) {
            int i = 0;
            for (ProductivityLine line : olines) {
                tableModel.insertRow(i, new Object[]{line.getKey(), line.getValue()});
                i++;
            }
        }
    }

