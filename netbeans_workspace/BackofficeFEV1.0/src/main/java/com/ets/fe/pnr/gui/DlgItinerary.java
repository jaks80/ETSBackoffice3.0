    private class ThreadSave implements Runnable {

        private Itinerary _segment;

        public ThreadSave(Itinerary segment) {
            this._segment = segment;
        }

        @Override
        public void run() {
            busyLabel.setBusy(true);
            ItineraryWSClient client = new ItineraryWSClient();
            segment = client.update(segment);
            btnSave.setEnabled(true);
            busyLabel.setBusy(false);
            busyLabel.setText("Updated...");
        }
    }
