    private void save() {
        btnSave.setEnabled(false);
        segment.setDeptFrom(txtDptFrom.getText().trim());
        segment.setDeptTo(txtDptTo.getText().trim());
        segment.setDeptDate(dtDptDate.getDate());
        segment.setDeptTime(txtDptTime.getText().trim());
        segment.setArvDate(dtArvDate.getDate());
        segment.setArvTime(txtArvTime.getText().trim());
        segment.setTicketClass(txtClass.getText().trim());
        segment.setFlightNo(txtFlNo.getText().trim());
        segment.setTktStatus(txtStatus.getText().trim());
        segment.setBaggage(txtBaggage.getText().trim());
        segment.setAirLineCode(txtCareer.getText().trim());
        segment.setFlightDuration(txtFlDuration.getText().trim());
        segment.setCheckInTerminal(txtChkInTerminal.getText().trim());
        segment.setCheckInTime(txtChkInTime.getText().trim());
        segment.setMealCode(txtMealCode.getText().trim());
        segment.setMileage(txtMilage.getText().trim());

        Thread t1 = new Thread(new ThreadSave(segment));
        t1.start();
    }

    private void populateSegment(Itinerary s) {
        txtDptFrom.setText(s.getDeptFrom());
        txtDptTo.setText(s.getDeptTo());
        dtDptDate.setDate(s.getDeptDate());
        txtDptTime.setText(s.getDeptTime());
        dtArvDate.setDate(s.getArvDate());
        txtArvTime.setText(s.getArvTime());
        txtClass.setText(s.getTicketClass());
        txtFlNo.setText(s.getFlightNo());
        txtStatus.setText(s.getTktStatus());
        txtBaggage.setText(s.getBaggage());
        txtCareer.setText(s.getAirLineCode());
        txtFlDuration.setText(s.getFlightDuration());
        txtChkInTerminal.setText(s.getCheckInTerminal());
        txtChkInTime.setText(s.getCheckInTime());
        txtMealCode.setText(s.getMealCode());
        txtMilage.setText(String.valueOf(s.getMileage()));
    }

    @Override
    public void actionPerformed(ActionEvent evt) {
        Object source = evt.getSource();
        if (source == btnSave) {
            save();
            save = true;
            setVisible(false);
        } else {
            dispose();
        }
    }

